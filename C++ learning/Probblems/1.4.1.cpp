#include<iostream>
#include<math.h>

using namespace std;
int main(){
    float x;
    float y;
    float z;
    cout << "x = ";
    cin >> x;
    cout << "y = ";
    cin >> y;
    cout << "z = ";
    cin >> z;
    cout << "sin(x) = " << sin(x) << endl;
    cout << "sqrt(y) = " << sqrt(y) << endl;
    cout << "log(z) = " << log10(z) << endl;
    return 0;
}